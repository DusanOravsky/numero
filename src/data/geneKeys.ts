const geneKeyDicts: Record<Language, GeneKey[]> = { sk: geneKeys, en: enGeneKeys };

export function getGeneKeyByGate(gate: number, lang: Language = 'sk'): GeneKey | undefined {
  const keys = geneKeyDicts[lang] ?? geneKeyDicts.sk;
  return keys.find(gk => gk.gate === gate);
}

export function getGeneKeysForGates(gates: number[], lang: Language = 'sk'): GeneKey[] {
  const keys = geneKeyDicts[lang] ?? geneKeyDicts.sk;
  return gates.map(g => keys.find(gk => gk.gate === g)).filter((gk): gk is GeneKey => gk !== undefined);
}
